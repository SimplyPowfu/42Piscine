int	ft_iterative_factorial(int nb)
{
	int	i;
	int	fact;

	i = 1;
	fact = 1;
	if (nb > 0)
	{
		while (i <= nb)
		{
			fact *= i;
			i++;
		}
	}
	return (fact);
}
